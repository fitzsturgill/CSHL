function rgb = mycolors(condition)
    if nargin < 1
        condition = '';
    end
    color_table = {...
        'chat', [171 55 214]/255;... % purple
        'dat', [237 125 49]/255;...
        'licks', [0 0 0];...
        'shock', [0 158 115]/255;
        'shock_cued', [153 153 51]/255;... 
        'reward', [0 114 178]/255;...
        'reward_cued', [86 180 233]/255;...
        'puff', [213 94 0]/255;...
        'puff_cued', [204 121 167]/255;...        
        };
   
    row = find(strcmpi(color_table(:,1), condition));
    if row
        rgb = color_table{row, 2};
    else
        disp('choices:');
        disp(color_table(:,1));
    end
    